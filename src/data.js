export function getPromotion(id) {
  return promotions.find((promotion) => promotion.id === id) ?? promotions[0];
}

export function getPromotionYears(promotion) {
  const datedYears = [
    ...promotion.ppvs.map((event) => event.year),
    ...promotion.shows.flatMap((show) => [
      new Date(`${show.startDate}T00:00:00`).getFullYear(),
      new Date(`${show.endDate ?? show.startDate}T00:00:00`).getFullYear(),
    ]),
  ].filter(Boolean);

  if (!datedYears.length) return years;

  const minYear = Math.min(...datedYears);
  const maxYear = Math.max(...datedYears);
  return Array.from({ length: maxYear - minYear + 1 }, (_, index) => minYear + index);
}

export function getYearEvents(promotion, year) {
  const weekly = promotion.shows.flatMap((show) => buildEpisodes(show, year));
  const ppvs = promotion.ppvs.filter((event) => event.year === year);
  return [...weekly, ...ppvs].sort((a, b) => a.date.localeCompare(b.date));
}

export function getShowEpisodes(show, year) {
  return buildEpisodes(show, year);
}

